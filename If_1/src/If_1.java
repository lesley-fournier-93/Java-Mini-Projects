
public class If_1 {

	public static void main(String[] args) {
	      int a = 5; // Du kannst hier verschiedene Werte ausprobieren

	        // Prüfen, ob a gleich 5 ist
	        if (a == 5)
	            System.out.println("a ist gleich 5");

	        // Prüfen, ob a kleiner als 5 ist
	        if (a < 5)
	            System.out.println("a ist kleiner als 5");

	        // Prüfen, ob a größer als 5 ist
	        if (a > 5)
	            System.out.println("a ist größer als 5");

	}

}
