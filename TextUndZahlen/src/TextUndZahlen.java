/*
 * Beispiel für Mehrfachausgabe
 * mit Berechnung in der Ausgabeanweisung
 */

public class TextUndZahlen {

	public static void main(String[] args) {
		int a=3, b=2;	// Dekl. u. Init. zweier Variablen

		System.out.println
		("Gegeben sind zwei Zahlen: "+a+" und "+b);
		System.out.println
		("Die Summe der Zahlen "+a+" und "+b+" ist "+(a+b));
		System.out.println
		("Das Produkt der Zahlen "+a+" und "+b+" ist "+(a*b));
		System.out.println
		("Die Differenz der Zahlen "+a+" und "+b+" ist "+(a-b));
	}
}
