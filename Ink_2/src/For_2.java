class For_2 {
    public static void main(String[] args) {
        System.out.println(); // Zeilenvorschub vor der Ausgabe

        // Schleife zählt rückwärts von 10 bis 1
        for (int i = 10; i >= 1; i--) {
            System.out.print(i + "  "); // zwei Leerzeichen zwischen den Zahlen
        }

        System.out.println(); // Zeilenvorschub nach der Ausgabe
    }
}
