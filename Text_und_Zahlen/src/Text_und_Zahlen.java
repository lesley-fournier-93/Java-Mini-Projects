/*
 * Beispiel für Mehrfachausgabe
 * mit Berechnung in der Ausgabeanweisung
 */
public class Text_und_Zahlen {

	 public static void main(String[] args) {
	        int zahl1 = 3, zahl2 = 2; // Dekl. u. Init. zweier Variablen

	        System.out.println("Gegeben sind zwei Zahlen: " + zahl1 + " und " + zahl2);
	        System.out.println("Die Summe der Zahlen " + zahl1 + " und " + zahl2 + " ist " + (zahl1 + zahl2));
	        System.out.println("Das Produkt der Zahlen " + zahl1 + " und " + zahl2 + " ist " + (zahl1 * zahl2));
	        System.out.println("Die Differenz der Zahlen " + zahl1 + " und " + zahl2 + " ist " + (zahl1 - zahl2));
	    }

}
